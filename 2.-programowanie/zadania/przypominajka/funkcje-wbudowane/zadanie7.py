## Zadanie 7: Użycie funkcji `divmod`

# **Cel:** Dla danych dwóch liczb, znajdź iloraz i resztę z dzielenia.

liczba_1 = 69
liczba_2 = 419
iloraz = liczba_2 // liczba_1
reszta = liczba_2 % liczba_1
(divmod_iloraz, divmod_reszta) = divmod(liczba_2, liczba_1)

print(iloraz, reszta)
print(divmod_iloraz, divmod_reszta)
