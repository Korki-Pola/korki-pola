val_1 = float(input('Wpisz pierwszą dowolną liczbę: '))
val_2 = float(input('Wpisz drugą dowolną liczbę: '))

suma = val_1 + val_2
roznica = val_1 - val_2
iloraz = val_1 / val_2
iloczyn = val_1 * val_2

print(f'Suma to: {suma}.')
print(f'Różnica to: {roznica}.')
print(f'Iloraz to: {iloraz}.')
print(f'Iloczyn to: {iloczyn}.')

# float // float -> float
#  12.0 // 5.0 -> 2.0
# float / float -> float
#  12.0 / 5.0 -> 2.4



